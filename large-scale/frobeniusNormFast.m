%       Returns the Frobenius norm of matrix U*V', using the trace inner
%       product formulation, which can be exploited to make computing the
%       Frobenius norm much faster than norm(U*V','fro') when U*V' is a
%       large low-rank matrix.
% 
%       Here low-rank means that the common dimension of U and V is small,

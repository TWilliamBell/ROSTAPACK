function delta = halleyStep(f,df,ddf)
%   halleyStep(d,df,ddf):
%       Given f(x_k), f'(x_k), and f''(x_k), this returns the step delta
%       for Halley's method for 1D root finding: x_k+1 = x_k + delta.
%
%   See also newtonStep and rootStep.
%
%
%   For comments/bug reports, please visit the ROSTAPACK GitLab webpage:
%   https://gitlab.com/timmitchell/ROSTAPACK
